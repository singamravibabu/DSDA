# Create two lists named books1, and books2, with 3 books each.
books1 = ['Book A', 'Book B', 'Book C']
books2 = ['Book D', 'Book E', 'Book F']

# Concatenate the two lists using the + operator
books_combined = books1 + books2
print(books_combined)

# Concatenate the two lists using the += operator
books1 += books2
print(books1)
