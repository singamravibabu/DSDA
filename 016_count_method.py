# Create a list with 8 strings for count() method examples
list_of_strings = [
    "apple", "banana", "cherry", "apple", "fig", 
    "fig", "grape", "fig"
]

# Count the occurrences of 'fig' in the list
print("Fig count:", list_of_strings.count("fig"))

# Count the occurrences of 'apple' in the list
print("Apple count:", list_of_strings.count("apple"))

# Count the occurrences of 'mango' in the list
print("Mango count:", list_of_strings.count('mango'))