# Create a list of random integers: +ve, -ve, and zero
numbers = [3, -1, 0, 7,
            -5, 2, 0, -3,
            8, -2, 4, 0,
            -6, 1, 5, -4]

# find the minimum value
min_value = min(numbers)
print("Minimum value:", min_value)

# find the maximum value
max_value = max(numbers)
print("Maximum value:", max_value)