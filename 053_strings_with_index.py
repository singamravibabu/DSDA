city = 'hyderabad'

print(city[0])   # Accessing the first character
print(city[1])   # Accessing the second character
print(city[2])   # Accessing the third character
print(city[-1])  # Accessing the last character (negative indexing)
print(city[-2])  # Accessing the second to last character
print(city[-3])  # Accessing the third to last character
