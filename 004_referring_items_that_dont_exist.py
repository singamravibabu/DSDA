# list with seven flowers
flowers = ["Rose", "Tulip", "Daisy", "Sunflower", "Lily", "Orchid", "Daffodil"]

# refer index 7
print(flowers[7])  # This will raise an IndexError because there is no index 7 in the list

# refer index -8
print(flowers[-8])  # This will also raise an IndexError because there is no index -8 in the list